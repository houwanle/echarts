package com.czy.beans;

public class Treemap {
	private String id;
	private String mapDesc;
	private String borderWidth;
	private String borderColor;
	private String mapHeight;
	private String typeaxis;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMapDesc() {
		return mapDesc;
	}
	public void setMapDesc(String mapDesc) {
		this.mapDesc = mapDesc;
	}
	public String getBorderWidth() {
		return borderWidth;
	}
	public void setBorderWidth(String borderWidth) {
		this.borderWidth = borderWidth;
	}
	public String getBorderColor() {
		return borderColor;
	}
	public void setBorderColor(String borderColor) {
		this.borderColor = borderColor;
	}
	public String getMapHeight() {
		return mapHeight;
	}
	public void setMapHeight(String mapHeight) {
		this.mapHeight = mapHeight;
	}
	public String getTypeaxis() {
		return typeaxis;
	}
	public void setTypeaxis(String typeaxis) {
		this.typeaxis = typeaxis;
	}
}
