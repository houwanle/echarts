package com.czy.beans;

public class Pie {
	private String id;
	private String innerRadius;
	private String externalRadius;
	private String piePositionRadio;
	private String legendPositionRadio;
	private String typeaxis;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getInnerRadius() {
		return innerRadius;
	}
	public void setInnerRadius(String innerRadius) {
		this.innerRadius = innerRadius;
	}
	public String getExternalRadius() {
		return externalRadius;
	}
	public void setExternalRadius(String externalRadius) {
		this.externalRadius = externalRadius;
	}
	public String getPiePositionRadio() {
		return piePositionRadio;
	}
	public void setPiePositionRadio(String piePositionRadio) {
		this.piePositionRadio = piePositionRadio;
	}
	public String getLegendPositionRadio() {
		return legendPositionRadio;
	}
	public void setLegendPositionRadio(String legendPositionRadio) {
		this.legendPositionRadio = legendPositionRadio;
	}
	public String getTypeaxis() {
		return typeaxis;
	}
	public void setTypeaxis(String typeaxis) {
		this.typeaxis = typeaxis;
	}
}
